char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	chr;

	chr = (char)c;
	i = 0;
	while (s[i] && s[i] != chr)
	{
		i++;
	}
	if (s[i] == '\0' && chr != '\0')
		return (0);
	return ((char *)(s + i));
}
