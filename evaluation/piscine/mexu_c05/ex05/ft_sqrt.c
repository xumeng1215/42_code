#include <stdio.h>

int	ft_sqrt(int nb)
{
	long	i;

	i = 1;
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}

int	main(void)
{
	printf("%d\n", ft_sqrt(-30));
	return (0);
}

