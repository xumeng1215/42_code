#include <unistd.h>
#include <stdio.h>

int	ft_str_is_printable(char *str)

{
	int	id;

	id = 0;
	while (str[id] != '\0')
	{
		if (!(str[id] >= 32 && str[id] <= 126))
		{
			return (0);
		}
		id++;
	}
	return (1);
}
