#include <stdio.h>
#include <unistd.h>

int	ft_str_is_numeric(char *str)

{
	int	id;

	id = 0;
	while (str[id] != '\0')
	{
		if (!(str[id] >= '0' && str[id] <= '9'))
			return (0);
		id++;
	}
	return (1);
}
