#include <unistd.h>
#include <stdio.h>

int	ft_str_is_alpha(char *str)

{
	int	id;

	id = 0;
	while (str[id] != '\0')
	{
		if (!((str[id] >= 'A' && str[id] <= 'Z')
				|| (str[id] >= 'a' && str[id] <= 'z')))
		{
			return (0);
		}
		id++;
	}
	return (1);
}
