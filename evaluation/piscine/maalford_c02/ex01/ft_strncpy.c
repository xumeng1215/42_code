#include <string.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

#include <stdio.h>
int main(void)
{
	char str[] = "world";

	printf("%s\n", str);
	ft_strncpy(str, "xxxxd", 10);
	printf("%s\n", str);
	return(0);
}
