#include "libft.h"

int	ft_isalnum(int arg)
{
	if (arg >= '0' && arg <= '9')
		return (1);
	if (arg <= 'z' && arg >= 'a')
		return (1);
	if (arg <= 'Z' && arg >= 'A')
		return (1);
	return (0);
}
